"""
In this project we are going to create a simple Calculator program.
This program will do basic mathematical operations like add, subtract, multiply and divide
"""


def add(n1, n2):
    return n1 + n2


def subtract(n1, n2):
    return n1 - n2


def multiply(n1, n2):
    return n1 * n2


def divide(n1, n2):
    return n1 / n2


